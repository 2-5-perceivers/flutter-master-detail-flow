/// This class is used to define some breakpoints for the master  detail flow.
/// Best practice is to use [MDBreakpoint.medium]
class MDBreakpoint {
  /// The most sensible breakpoint
  static const small = 600;

  /// The medium breakpoint
  static const medium = 700;

  /// The large breakpoint
  static const large = 800;

  /// The xlarge breakpoint
  static const xlarge = 1000;
}
