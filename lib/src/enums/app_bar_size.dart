/// Represents the size options for the app bar in MD widgets.
enum MDAppBarSize {
  /// No app bar.
  none,

  /// A small app bar represented by [SliverAppBar].
  small,

  /// A medium app bar represented by [SliverAppBar.medium].
  medium,

  /// A large app bar represented by [SliverAppBar.large].
  large,
}
